#include <iostream>

using namespace std;

/*
float 형으로 433.4892와 16.3953사칙연산 수행
*/
int main(void) {
	float a = 433.4892, b = 16.3953;

	cout << a << " + " << b << " = " << a + b << endl;
	cout << a << " - " << b << " = " << a - b << endl;
	cout << a << " * " << b << " = " << a * b << endl;
	cout << a << " / " << b << " = " << a / b << endl;

	return 0;
}
